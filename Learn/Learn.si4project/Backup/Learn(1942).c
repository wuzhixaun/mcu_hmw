// 单片机芯片初始化
void initMCU(){
	OSCCON = 0B01110001;	//WDT 32KHZ IRCF=111=16MHZ
					 		//Bit0=1,系统时钟为内部振荡器
					 		//Bit0=0,时钟源由FOSC<2：0>决定即编译选项时选择
	INTCON = 0;  			//暂禁止所有中断
 	TRISC7 = 0; // 0是输出，1是输入
	LATC7 = 1;

}
